import generatePDF from "../utility/CreatePdf";

const ExportBtn = ({tabledata, type}) => {
    
const handleClick = ()=> {
    if (tabledata.length != 0) {
        generatePDF(tabledata, type)
    }
}    
    return (
      <button
        id="dropdown-button"
        className="inline-flex justify-center ml-auto  w-52 px-4 py-2 text-sm font-medium text-gray-700 bg-white border border-gray-300 rounded-md shadow-sm focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-offset-gray-100 focus:ring-blue-500"
        onClick={handleClick}
      >
        Export
      </button>
    );
}
 
export default ExportBtn;